print(10 + 3)  # addition
print(10 - 3)  # subtraction
print(10 * 3)  # multiplication
print(10 / 3)  # division. float(decimal)
print(10 // 3)  # division. integer
print(10 % 3)  # modulus. returns the remainder of a division
print(10 ** 3)  # exponent. 10 raised to power 3
# for all these operators, we have augmented assignment operator.

# AUGMENTED ASSIGNMENT OPERATORS
x = 10
# x = x + 3 # not augmented
# x += 3  # augmented
# x -= 3
x *= 3
print(x)
