# tuples are similar to list, so we can use them to store a list of items. Unlike list, we cannot modify them, we cannot add new items, we cannot remove existing items. They are immutable

# tuples are unchangeable list

# we use [] for list and () for tuples
numbers = (1, 2, 3)
# we only have 2 methods for tuple, count() and index()

# methods with __ are called magic methods
# tuples are read only
